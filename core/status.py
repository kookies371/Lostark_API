from enum import Enum, auto


class Status(Enum):
    CRITICAL = auto()  # 치명
    SPECIALITY = auto()  # 특화
    AGILITY = auto()  # 신속
    SUBDUE = auto()  # 제압
    ENDURANCE = auto()  # 인내
    PROFICIENCY = auto()  # 숙련
    INTELLIGENCE = auto()  # 지성
    KINDNESS = auto()  # 친절
    CHARM = auto()  # 매력
    COURAGE = auto()  # 담력
